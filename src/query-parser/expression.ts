import { useOperators, OperatorType } from 'mingo/core';
import {
  $abs,
  $add,
  $and,
  $arrayElemAt,
  $ceil,
  $cmp,
  $concat,
  $cond,
  $divide,
  $eq,
  $floor,
  $gt,
  $gte,
  $ifNull,
  $in,
  $indexOfBytes,
  $literal,
  $lt,
  $lte,
  $ltrim,
  $mod,
  $ne,
  $nin,
  $not,
  $or,
  $multiply,
  $round,
  $rtrim,
  $size,
  $split,
  $sqrt,
  $strLenBytes,
  $strcasecmp,
  $substr,
  $subtract,
  $switch,
  $toBool,
  $toDouble,
  $toInt,
  $toLong,
  $toLower,
  $toString,
  $toUpper,
  $trim,
  $trunc,
  $type,
} from 'mingo/operators/expression';

export function initExpressionOperators() {
  // eslint-disable-next-line react-hooks/rules-of-hooks
  useOperators(OperatorType.EXPRESSION, {
    $abs,
    $add,
    $and,
    $arrayElemAt,
    $ceil,
    $cmp,
    $concat,
    $cond,
    $divide,
    $eq,
    $floor,
    $gt,
    $gte,
    $ifNull,
    $in,
    $indexOfBytes,
    $literal,
    $lt,
    $lte,
    $ltrim,
    $mod,
    $ne,
    $nin,
    $not,
    $or,
    $multiply,
    $round,
    $rtrim,
    $size,
    $split,
    $sqrt,
    $strcasecmp,
    $strLenBytes,
    $substr,
    $subtract,
    $switch,
    $toBool,
    $toDouble,
    $toInt,
    $toLong,
    $toLower,
    $toString,
    $toUpper,
    $trim,
    $trunc,
    $type,
  });
}
